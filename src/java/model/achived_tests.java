@Entity
public class achived_tests implements Serializable {

    @Id
    private int id;

    private int grade;

    @ManyToOne
    @JoinColumn(name = "user_id")
    user user;

    public user getUser() {
        return user;
    }

    public void setUser(user user) {
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }

}
